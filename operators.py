# Arithmetic Operators

x = 10
y = 4
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus -- Returns the remainder after division

#Comparison Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) # Equal to
print(x != y) # Not Equal to


#Assingment operators
w = 7
print(w)

w += 2
print(w)

w -= 2
print(w)

y /= 4
print(y)

#Logic Operators - And, Not , Or
print(34 < 4 and 56 > 21)
print(34 < 4 or 56 > 21)
print(not (34 < 4 or 56 > 21))